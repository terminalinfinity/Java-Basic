package Operators;

public class Increment {
    public static void main(String[] args) {
        int i=5;
        System.out.println("Before PostIncrement:"+i);
        System.out.println("    PostIncrement:"+i++);
        System.out.println("After PostIncrement:"+i);
        int j=10;
        System.out.println("Before PreIncrement:"+j);
        System.out.println("     PostIncrement:"+ ++j);
        System.out.println("Before PostIncrement:"+j);
    }
}
