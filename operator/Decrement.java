package Operators;

public class Decrement {
    public static void main(String[] args) {
        int i=5;
        System.out.println("Before PostDecrement:"+i);
        System.out.println("    PostDecrement:"+i--);
        System.out.println("After PostDecrement:"+i);
        int j=10;
        System.out.println("Before PreDecrement:"+j);
        System.out.println("     PostDecrement:"+ --j);
        System.out.println("Before PostDecrement:"+j);
    }
}
